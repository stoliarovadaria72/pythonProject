a = int(input("Введите номер места "))
if (a < 37) and (a % 2==0):
    print("Место верхнее в купе")
elif (a < 37) and (a % 2!=0):
    print("Место нижнее в купе")
elif (a >= 37) and (a <= 54) and (a % 2==0):
    print("Место боковое верхнеe")
elif (a >= 37) and (a <= 54) and (a % 2!=0):
    print("Место боковое нижнее")
else:
    print("Место не существует")
